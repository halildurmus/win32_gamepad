/// The compass point represented by the directional pad.
enum Direction {
  /// Left.
  west,

  /// Left and up.
  northwest,

  /// Up.
  north,

  /// Up and right.
  northeast,

  /// Right.
  east,

  /// Down and right.
  southeast,

  /// Down.
  south,

  /// Down and left.
  southwest,

  /// Neutral (not pressed).
  center
}
